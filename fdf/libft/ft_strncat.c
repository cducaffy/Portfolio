#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int				i;
	unsigned int	j;
	int				total;

	i = ft_strlen(s1);
	j = 0;
	total = ft_strlen(s1) + ft_strlen(s2);
	while (i < total && j < n)
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
